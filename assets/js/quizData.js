function Quiz(title, duration, image, questions) {
  var questionLength = 0;
  if (questions) questionLength = questions.length;

  this.title = title;
  this.totalQuestions = questionLength;
  this.duration = duration;
  this.image = image;
  this.questions = [];
  if (questions) {
    for (var i = 0; i < questionLength; i++) {
      var row = questions[i];
      this.questions.push(new Question(row[0], row[1], row[2]));
    }
  }
}

function Question(question, options, correctOption) {
  this.question = question;
  this.options = options;
  this.correctOption = correctOption;
  this.answer = null;
}
